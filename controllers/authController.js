const signToken = (id) => {
  return jwt.sign({ id }, process.env.JWT_SECRET, {
    expiresIn: process.env.JWT_EXPIRES_IN,
  });
};

const createSendToken = (user, statusCode, res) => {
  const token = signToken(user._id);
  const cookieOptions = {
    expires: new Date(
      Date.now() + process.env.JWT_COOKIE_EXPIRES_IN * 24 * 60 * 60 * 1000,
    ),
    httpOnly: true,
    sameSite: 'Lax',
  };

  if (process.env.NODE_ENV === 'production') cookieOptions.secure = true;
  res.cookie('jwt', token, cookieOptions);
  // note that cookies can overwriten by thire names (The identefire for cookies is thire names !!) , Last cookie is 'jwt'

  // Expose the token as an Authorization response header for clients that need it
  res.setHeader('Authorization', `Bearer ${token}`);

  // Remove the password from output
  user.password = undefined;
  // StatusCode for created
  res.status(statusCode).json({
    status: 'success',
    token,
    data: {
      user,
    },
  });
};

